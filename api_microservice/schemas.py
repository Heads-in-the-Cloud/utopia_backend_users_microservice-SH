from typing import List, Optional

from pydantic import BaseModel


# ------------------------------------------------
#                       Users
# ------------------------------------------------


class User(BaseModel):
    role_id: int
    username: str
    email: str
    given_name: str
    family_name: str
    password: str
    phone: str


class UserFull(User):
    id: int

    class Config:
        orm_mode = True


# ------------------------------------------------
#                      User Roles
# ------------------------------------------------


class UserRole(BaseModel):
    name: str


class UserRoleFull(UserRole):
    id: int

    class Config:
        orm_mode = True
